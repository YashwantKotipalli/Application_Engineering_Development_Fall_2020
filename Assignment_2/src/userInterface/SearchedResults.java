    private void populateTable2(){
        DefaultTableModel dtm = (DefaultTableModel) tblAccounts2.getModel();
        dtm.setRowCount(0);
        
        for(Account account: accountList2.getAccountList2()){
            Object[] row = new Object[7];
            row[0] = account.getCarName();
            row[1]=account.getManufacturer();
            row[2] = account.getYom();
            row[3]= account.getNumberOfSeats();
            row[4] = account.getModelNumber();
            row[5] = account.getSerialNumber();
            row[6] = account.getLocation();
            dtm.addRow(row);
            
            
        }
    }

