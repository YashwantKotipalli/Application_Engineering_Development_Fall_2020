    private void btnViewDetailsActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnViewDetailsActionPerformed
        // TODO add your handling code here:
        int selectedRow = tblAccounts2.getSelectedRow();

        if(selectedRow<0){

            JOptionPane.showMessageDialog(null, "Please select a row to be viewed", "Warning", JOptionPane.WARNING_MESSAGE);

        }
        else{

            Account account = (Account)tblAccounts2.getValueAt(selectedRow, 0);
            ViewAccountJPanel panel  = new ViewAccountJPanel(userProcessContainer, account);
            userProcessContainer.add("ViewAccountJPanel", panel);
            CardLayout layout = (CardLayout) userProcessContainer.getLayout();
            layout.next(userProcessContainer);

        }

    }//GEN-LAST:event_btnViewDetailsActionPerformed

    private void btnDeleteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnDeleteActionPerformed
        // TODO add your handling code here:

        int selectedRow = tblAccounts2.getSelectedRow();
        if(selectedRow>=0){

            int dialogButton = JOptionPane.YES_NO_OPTION;
            int dialogResult = JOptionPane.showConfirmDialog(null, "Would you like to delete the car entry ? ", "Warning", dialogButton);

            if(dialogResult == JOptionPane.YES_OPTION){

                Account account = (Account) tblAccounts2.getValueAt(selectedRow, 0);
                accountDirectory.deleteAccount(account);
                populateTable2();
            }
        }
        else{
            JOptionPane.showMessageDialog(null, "Please select a row to be deleted first", "Warning", JOptionPane.WARNING_MESSAGE);
        }
    }//GEN-LAST:event_btnDeleteActionPerformed

